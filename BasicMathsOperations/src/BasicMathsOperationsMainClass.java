public class BasicMathsOperationsMainClass {  //Class
    public static void main(String[] args) {  //Main Class
        int n1 = 10;  //Variable for first value.
        int n2 = 2;  //Variable for second value.
        //Maths operations:
        System.out.println("N1 + N2 = "+(n1+n2));
        System.out.println("N1 - N2 = "+(n1-n2));
        System.out.println("N1 * N2 = "+(n1*n2));
        System.out.println("N1 / N2 = "+(n1/n2));
        System.out.println("N1 % N2 = "+(n1%n2));
        System.out.println("N1 + 1 = "+(++n1));
    }
}
